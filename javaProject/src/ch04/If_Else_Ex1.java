package ch04;

public class If_Else_Ex1 {

	public static void main(String[] args) {
		// if elas문
		int score = 90; //정수 초기화
		String result = ""; //문자열을 초기화만 해놓은 상태
		if (score >= 60) {
			result = "합격";
		} else {
			result = "불합격";
		}
		
		System.out.println(result);
		
		
		
	}

}
